def velocidade_media(distancia, tempo):
    velocidade = distancia/tempo
    return velocidade

def soma(x, y):
    return x + y

def subtracao(x, y):
    return x - y

def calculadora(x, y):
    return soma(x,y), subtracao(x,y), x*y, x/y

def divisao(x, y):
    return x/y

def vel_media(distancia, tempo):
    velocidade = divisao(distancia, tempo)
    return velocidade
